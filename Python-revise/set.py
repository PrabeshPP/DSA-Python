# A set is a collection which is onordered and unindexed.No duplicate members

#create a set
fruit_set={'Apples','Oranges','Mango'}

print('Apples' in fruit_set)

#Add to set
fruit_set.add('Grape')

#remove from set
fruit_set.remove('Grape')

#Clear set
fruit_set.clear()

#delete set and undefining the set

del fruit_set



#clear set